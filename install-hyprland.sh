print_command() {
    local command="$1"
    echo -e "${GREEN}${GEAR} –ö–æ–º–∞–Ω–¥–∞ –¥–ª—è –≤—ã–ø–æ–ª–Ω–µ–Ω–∏—è:${NC}"
